class Pessoa {
    public String nome, email, cep, estado, pais, descricao

    Pessoa(String nome, String email, String cep, String estado, String pais, String descricao) {
        this.nome = nome
        this.email = email
        this.cep = cep
        this.estado = estado
        this.pais = pais
        this.descricao = descricao
    }
}